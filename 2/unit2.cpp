#include <iostream>

int main() {
    std::cout << 1 + 1 << "%\n";

    // unit9
    std::cout << 5 * 10 / 2 << "\n";
    std::cout << 2100000000LL * 210000000LL << "\n";

    // unit10
    std::cout << 10 / 3 << "\n";
    std::cout << 10.0 / 3.0 << "\n";

    // unit11
    std::cout << 10 % 3 << "\n";
    std::cout << 4 % 2 << "\n";

    // unit12
    std::cout << 2 + 2 * 5 << "\n";
    std::cout << (2 + 2) * 5 << "\n";

    // step up
    std::cout << 1 - 3 - 5 + 8 << "\n";
}